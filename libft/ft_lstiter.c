#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *tmp;

	tmp = NULL;
	if (lst)
		tmp = lst;
	while (tmp)
	{
		f(tmp);
		tmp = tmp->next;
	}
}
