#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*ret;

	ret = NULL;
	while (1)
	{
		if (*s == c)
			ret = (char *)s;
		if (*s++ == '\0')
			return (ret);
	}
}
