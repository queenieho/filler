#include "libft.h"

void	ft_lstadd(t_list **alst, t_list *new)
{
	t_list *tmp;

	tmp = *alst;
	if (*alst)
	{
		new->next = *alst;
		tmp = new;
		*alst = tmp;
	}
	else
		*alst = new;
}
